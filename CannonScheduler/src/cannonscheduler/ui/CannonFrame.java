    private void addCSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCSVActionPerformed
                                                       
        PasswordDialog passwordGUI = new PasswordDialog(this,true);
        passwordGUI.setVisible(true);
        passwordGUI.setLocationRelativeTo(null);
        
        
        boolean success = passwordGUI.getSuccess();
        
        if (success) {
            JFileChooser addFileChooser = new JFileChooser();
            int option = addFileChooser.showSaveDialog(this);
            if (option == JFileChooser.APPROVE_OPTION){ // save location of file
                fileLocation = addFileChooser.getSelectedFile().getPath();
                updateRequestList(fileLocation);
            } else if (option == JFileChooser.CANCEL_OPTION){
                addFileChooser.cancelSelection();
            }
        }
    }//GEN-LAST:event_addCSVActionPerformed

