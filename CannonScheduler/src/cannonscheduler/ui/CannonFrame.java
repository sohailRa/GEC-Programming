    public void updateRequestList(String filePath){
        int i;
        Parser parser = new Parser(filePath);
        if (parser != null){
            for(i=0;i<parser.getRequests().size();i++){ // For the length of the parsed array
                
                if (requestList.size() == 0) requestList.add(parser.getRequests().get(i)); // add to the list of requests
                
                boolean repeatedNameFlag = false;
                for(int j = 0; j < requestList.size(); j++) {
                    if(requestList.get(j).getGroupName().equals(parser.getRequests().get(i).getGroupName())){ // if the current list does not contain a request in the lst (avoids duplicates)
                        repeatedNameFlag = true;
                    }
                }
                
                if (!repeatedNameFlag) requestList.add(parser.getRequests().get(i)); // add to the list of requests
            }
        }
        for(Request r: requestList)
            System.out.println(r);
        
    }

